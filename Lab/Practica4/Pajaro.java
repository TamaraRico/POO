
public class Pajaro {
	String nombre;
	String forma;
	
	public Pajaro(String nombre, String forma) {
		this.nombre=nombre;
		this.forma=forma;
	}
	
	public void setNombre(String nombre) {
		this.nombre=nombre;
	}
	
	public void setForma(String forma) {
		this.forma=forma;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public String getForma() {
		return forma;
	}
	
}
